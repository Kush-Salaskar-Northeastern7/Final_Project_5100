    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
        if(txtLtname.getText().isEmpty() || txtLtusername.getText().isEmpty() || txtLtpassword.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(this, "Please fill all details.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String name = txtLtname.getText();
        String username = txtLtusername.getText();
        String password = String.valueOf(txtLtpassword.getPassword());
        
        if(!ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(username)){
            JOptionPane.showMessageDialog(null, "Username already exists, select a new username", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Employee employee = ecosystem.getEmployeeDirectory().createEmployee(name);       
        UserAccount userAccount = ecosystem.getUserAccountDirectory().createUserAccount(username, password, employee, new LabTechnicianRole());
        LabTechnician lt = ecosystem.getLabTechnicianDirectory().addLabTechnician(name,userAccount, userAcc.getUid());    
        
        txtLtname.setText("");
        txtLtpassword.setText("");
        txtLtusername.setText("");
        populateTable();
        JOptionPane.showMessageDialog(null, "Lab Technician Created");
        
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblLabTechs.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select any row.", "Error", JOptionPane.WARNING_MESSAGE);
        } 
        else {
            LabTechnician lt = (LabTechnician) tblLabTechs.getValueAt(selectedRow, 0);
            UserAccount ua = lt.getUserAccount();
            ecosystem.getUserAccountDirectory().deleteUserAccount(ua); //need to delete useraccount of lab technician as well
            ecosystem.getLabTechnicianDirectory().deleteLabTechnician(lt);
            JOptionPane.showMessageDialog(null, "Lab Technician removed Successfully.");
            populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed
    }
